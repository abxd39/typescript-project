namespace variableDeclaration{


//变量声明
// let 变量名: 类型 = 值;

// Declare and initialize a variable of type number
let age: number = 25;

// Declare and initialize a variable of type string
let name: string = "John Doe";

// Declare and initialize a variable of type boolean
let isStudent: boolean = true;

// Declare and initialize a variable of type array
let numbers: number[] = [1, 2, 3, 4, 5];

// Declare and initialize a variable of type object
let person: { name: string, age: number } = { name: "Jane Smith", age: 30 };
}